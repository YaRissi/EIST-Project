package de.tum.in.ase.insertteamnamehere.util;

public class Coord {
    float lat;
    float lon;

    public Coord(float lat,float lon){
        this.lat = lat;
        this.lon = lon;
    }

    public float getLat() {
        return lat;
    }

    public void setLat(float lat) {
        this.lat = lat;
    }

    public float getLon() {
        return lon;
    }

    public void setLon(float lon) {
        this.lon = lon;
    }
}
